let value = "A";

if (value >= "A" && value <= "Z") {
 
    console.log("Uppercase letter");

} 
else if (value >= "a" && value <= "z") {

    console.log("Lowercase letter");

} 
else if (value >= "0" && value <= "9") {
 
    console.log("Digit");

} 
else {

    console.log("Special value character");

}
